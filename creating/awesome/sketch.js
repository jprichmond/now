// setup canvas and initialize variables
function setup() {

  createCanvas(CANVAS_SIZE, CANVAS_SIZE);
  frameRate(FPS);
  angleMode(DEGREES);

  origin = createVector(0, 0);
  arena = new Arena();
  initGame();

  mouseX = width / 2;
  mouseY = height / 2;

} // end of setup()

// draw each frame on the canvas
function draw() {

  // lastLoop = millis(); // uncomment to see performance

  // START SCREEN //
  if (!gameStart) {
    startScreen();
  }

  // GAME STARTED //
  else {
    /////////////////////////////////// DRAW BACKGROUND ////
    setting();

    ////////////////////////////////////// SET MOVEMENT ////
    action();

    ///////////////////////////////// DETECT COLLISIONS ////
    conflict();

    ////////////////////////////////////// DRAW OBJECTS ////
    resolution();

    ///////////////////////////////////////// INTERFACE ////
    denouement();
  }

  performanceMeasures(lastLoop);

} // end of draw()


